public class Blocks {

    public static void main(String[] args) {
        
            int x = 0;
            if (x == 0) {
                 // then block
                 int y;
                 } else {
                 // else block
                 int y;
                 }
            while (x++ < 10) {
                 // while loop block
                 int y;
             }
             for (x = 1; x < 10; x++) {
                 int y;
             }
             {
                 //free block
                 int y;
             }
         }
     }




